// ─── Subcommand dispatch ──────────────────────────────────────────────────────

func cmdConfig(args []string, jsonOutput bool) error {
	if len(args) == 0 {
		return fmt.Errorf("config requires a subcommand.\n\nUsage: nav-pilot config <subcommand> [options]\n\nSubcommands:\n  init      Create ~/.nav-pilot/config.toml with all options commented out\n  show      Print effective configuration (file values merged with defaults)\n  path      Print the config file path\n  get       Print one key value\n  set       Set a key value (creates file if missing)\n  validate  Validate config syntax, unknown keys, and values\n  explain   Describe configuration keys")
	}

	sub := args[0]
	rest := args[1:]

	switch sub {
	case "init":
		return cmdConfigInit()
	case "show":
		return cmdConfigShow(jsonOutput)
	case "path":
		return cmdConfigPath()
	case "get":
		if len(rest) == 0 {
			return fmt.Errorf("config get requires a key.\n\nUsage: nav-pilot config get <key>\n\nKnown keys: %s", knownKeyNames())
		}
		return cmdConfigGet(rest[0])
	case "set":
		if len(rest) < 2 {
			return fmt.Errorf("config set requires a key and value.\n\nUsage: nav-pilot config set <key> <value>")
		}
		return cmdConfigSet(rest[0], rest[1])
	case "validate":
		return cmdConfigValidate()
	case "explain":
		key := ""
		if len(rest) > 0 {
			key = rest[0]
		}
		return cmdConfigExplain(key)
	default:
		return fmt.Errorf("unknown config subcommand: %q\n\nSubcommands: init, show, path, get, set, validate, explain", sub)
	}
}

// ─── config init ─────────────────────────────────────────────────────────────

func cmdConfigInit() error {
	path := configPath()

	if _, err := os.Stat(path); err == nil {
		return fmt.Errorf("config file already exists: %s\n\nUse %s to see current values, or edit the file directly.",
			path, bold("nav-pilot config show"))
	} else if !errors.Is(err, os.ErrNotExist) {
		return fmt.Errorf("checking config path: %w", err)
	}

	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return fmt.Errorf("creating config directory: %w", err)
	}
	if err := os.WriteFile(path, []byte(configInitTemplate), 0o644); err != nil {
		return fmt.Errorf("writing config: %w", err)
	}

	fmt.Printf("%s Created %s\n", green("✓"), path)
	fmt.Printf("  Edit the file or use %s to set individual options.\n", bold("nav-pilot config set"))
	return nil
}

// ─── config show ─────────────────────────────────────────────────────────────

